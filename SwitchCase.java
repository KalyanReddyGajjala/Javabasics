package Module1Progroms;

public class SwitchCase {

	public static void main(String[] args)
	{
		switch(5)
		{
		case 1:System.out.println("Chrome Browser");
		break;
		case 2:System.out.println("firefox browser");
		break;
		case 3:System.out.println("internet explorer");
		break;
        default:System.out.println("Sorry your selection is wrong");
        System.out.println("press 1 to chrome browser");
        System.out.println("press 2 to firefox browser");
        System.out.println("press 3 to internet explorer browser");
		



		}
		

	}

}
