package Module1Progroms;

public class ArithmaticMethodProgram 
{
	static void add()
	{
		int a=10;
		int b=20;
		int c=a+b;
		System.out.println("c="+c);
		
	}
	static void sub()
	{
		int a1=10;
		int b1=23;
		int c1=a1-b1;
		System.out.println("c1="+c1);
	}
	static void multi()
	{
		int a2=10;
		int b2=2;
		int c2=a2*b2;
		System.out.println("c2="+c2);
	}
	static void divison()
	{
		int a3=10;
		int b3=20;
		int c3=a3/b3;
		System.out.println("c3="+c3);
	}
	static void modules()
	{
		int a4=10;
		int b4=29;
		int c4=a4%b4;
		System.out.println("c4="+c4);
	}

	public static void main(String[] args)
	{
		add();
		sub();
		multi();
		divison();
		modules();
	
	}

}
