package Assignments;

public class Operators {

	public static void main(String[] args)
	{
		int a=10;
		int b=20;
		int c=a+b;
		int d=a-b;
		int e=a/b;
		int f=a*b;
		int g=a%b;
		System.out.println("addition of c="+c);
		System.out.println("Substraction of d="+d);
		System.out.println("division value e="+e);
		System.out.println("Multiple value f="+f);
		System.out.println("Modulus value g="+g);
		

	}

}
